public class area_of_a_triangle
{
    public static void main(String[] args)
    {
        //the formula for the area of a triangle is 1/2(b*h)

        int base = 4; //simply defines the base
        int height = 6; //simple defines the height

        int area = (base*height)/2; //defines area and then uses the formula to find it
        System.out.println(area); //tells us the result.
    }

}