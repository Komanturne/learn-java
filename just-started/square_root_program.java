public class square_root_program
{
    public static void main(String[] args)
    {
        int x = 9; //defines x

        int sqrt_x = (x*x); //x^2
        int cube_x = ((x*x)*x); //x^3
        System.out.println("the area of the square is: " + sqrt_x);
        System.out.println("the area of a cube with the same dimenstion is: " + cube_x);
    }
}